    'updated_at' => '2025-08-26 14:54:53',
  ),
);

        // Idempotent by NIP (updateOrCreate), agar aman dijalankan berulang
        foreach (array_chunk($rows, 500) as $chunk) {
            foreach ($chunk as $row) {
                // Jika NIP kosong, pakai create biasa
                if (!empty($row['nip'])) {
